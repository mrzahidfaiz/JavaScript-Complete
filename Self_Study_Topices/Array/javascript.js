//Pop 
console.log("pop Remove from last Index")
books.pop();
console.log(books);
//Push
console.log("push adds at last Index")
books.push("Medical");
console.log(books);
// unshift operation
console.log("unshift adds at first Index")
books.unshift("Computer Science");
console.log(books); // Insert on First Index
//shift
console.log("shift Remove 1st index value");
books.shift();
console.log(books);
//splice
console.log('splice remove no. of value by no. of Indexs')
books.splice(1, 1)
